def create_movie(title, genre, rating):
    new_movie = {
        "title": title,
        "genre": genre,
        "rating": rating
    }

    if title is None or genre is None or rating is None:
        return None

    return new_movie

def add_to_watched(user_data, movie):
    user_data["watched"].append(movie)
    
    return user_data

